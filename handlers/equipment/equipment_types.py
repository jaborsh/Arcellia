    """
    Defines the type of equipment.
    """

    AMULET = "amulet"
    ARMOR = "armor"
    CLOAK = "cloak"
    FOOTWEAR = "footwear"
    HANDWEAR = "handwear"
    HEADWEAR = "headwear"
    RING = "ring"
    SHIELD = "shield"
    WEAPON = "weapon"
